    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $parent = Parent_Category::findOrFail($id);
        $parent->parent_name = $request->parentname;
        $parent->save();
        Alert::success('Update Parent Category', 'Parent Category Berhasil diupdate');
        return redirect('admin/category');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $parent = Parent_Category::findOrFail($id);
        $parent->delete();
        Alert::warning('Delete Parent Category', 'Parent Category Berhasil dihapus');
        return redirect('admin/category');
    }
